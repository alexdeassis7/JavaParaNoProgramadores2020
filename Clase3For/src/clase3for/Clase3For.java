package clase3for;

public class Clase3For {

    public static void main(String[] args) {
        //FOR 
//         desde 0 -> hasta 1000
//        for(valor inicial ; condicion ; incremento )
        for(int j = 0 ; j <= 1000 ;  j+=2 ){          
            System.out.println(j);            
        }
    }

}
